def tweet_content(
    articles, tweeted_items, get_content, clean_content, tweet_format, max_tweets=1
):
    tweets_sent = 0
    for title, article_url in articles:
        if tweets_sent >= max_tweets:
            break

        if article_url in tweeted_items:
            email.send(f"Already tweeted: {article_url}")
            continue

        content_data = get_content(article_url)
        if content_data is None:
            email.send(f"Could not retrieve content for {article_url}")
            continue

        cleaned_data = clean_content(content_data[0])
        tweet_text = tweet_format(title, cleaned_data, article_url)
        bot.tweet(tweet_text)
        tweeted_items.add(article_url)

        # email.send(f"Tweeted: {tweet_text}")
        tweets_sent += 1


def fetch_and_tweet(
    url_format,
    max_pages,
    scrape_articles,
    tweeted_items,
    get_content,
    clean_content,
    tweet_format,
    max_tweets=1,
):
    try:
        for page_number in range(1, max_pages + 1):
            url = url_format.format(page_number=page_number)
            articles = scrape_articles(url)
            if not articles:
                email.send("No articles found.")
                return
            tweet_content(
                articles,
                tweeted_items,
                get_content,
                clean_content,
                tweet_format,
                max_tweets,
            )
            if len(tweeted_items) >= max_tweets:
                break
    except Exception as e:
        error_message = (
            f"An error occurred:\n"
            f"Last page number: {page_number}\n"
            f"Error Type: {type(e).__name__}\n"
            f"Error Message: {str(e)}\n"
            f"Traceback: {traceback.format_exc()}"
        )
        email.send(error_message)
        email.send(error_message)


